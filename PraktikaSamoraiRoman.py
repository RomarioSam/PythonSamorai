# Требуется вычислить, сколько раз встречается некоторое число X в массиве A[1..N].
# Пользователь в первой строке вводит натуральное число N – количество элементов в массиве.
# В последующих  строках записаны N целых чисел Ai. Последняя строка содержит число X
# *Пример:*
# 5
#     1 2 3 4 5
#     3
#     -> 1

N = int(input("Введите количество элементов в массиве: "))
mass = input("Введите через пробел числа в массиве: ").split(" ")[:N]
print(mass)
X = input("Введите число, которое хотите отыскать в вашем массиве: ")
count = 0
for i in mass:
    if i == X: count += 1
if count > 0:
    print(f"Ваше число есть в вашем массиве {count} раз")
else:
    print("Нет такого числа в вашем массиве")



